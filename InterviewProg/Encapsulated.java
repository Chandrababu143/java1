package InterviewProg;

public class Encapsulated {
	private String name;
	private int age;
	private int ifsc;
	private int pin;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name=name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age=age;
	}
	public int getIfsc() {
		return ifsc;
	}
	public void setIfsc(int ifsc) {
		this.ifsc=ifsc;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin=pin;
	}
	

}
